    private void lengthBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lengthBtnActionPerformed
        if(!jTextField1.getText().equals("") && jTextField2.getText().equals("")){
            foot = Double.parseDouble(jTextField1.getText());
            mile = foot / 5280.0;
            footLabel.setText("Foot: " + foot);
            mileLabel.setText("Mile: " + mile);
        }
        else if(jTextField1.getText().equals("") && !jTextField2.getText().equals("")){
            mile = Double.parseDouble(jTextField2.getText());
            foot = mile * 5280.0;
            footLabel.setText("Foot: " + foot);
            mileLabel.setText("Mile: " + mile);
        }
        else{
            footLabel.setText("Foot: ");
            mileLabel.setText("Mile: ");
            jTextField1.setText("");
            jTextField2.setText("");
        }
    }//GEN-LAST:event_lengthBtnActionPerformed

